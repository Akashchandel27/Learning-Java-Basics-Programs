package learning_java;

public class New_011_Variables1 {

	public static void main(String[] args) {
		//Local Variable
		
	    // int is a primitive data type, a is variable name, 5 is variable value
		int a=5;                   // Integer type local variable
		System.out.println(a);     // 5
		
		int x=100;               // Integer type local variable
		System.out.println(x);   // 100

		
		float y= (float) 300.2;         // float data type
		System.out.println(y);          // 300.2
		 
		char a1 = 'a';                  // single quote mai aata hai char data type
		System.out.println(a1);         // a

		String a2 = "akash";            // double quote mai aata hai string data type
		System.out.println(a2);         // akash
	}

}
