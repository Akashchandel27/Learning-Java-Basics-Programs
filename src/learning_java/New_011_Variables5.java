package learning_java;

public class New_011_Variables5 {

	public static void main(String[] args) {
		
		int x=10;                         // Defining a variable
		
		// Calling x variable for the printing its value
		System.out.println(x);            // 10
		
		// Calling x variable for initialize y
		int y=x;
		System.out.println(y);             //10

	}

}
