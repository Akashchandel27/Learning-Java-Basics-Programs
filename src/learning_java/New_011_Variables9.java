package learning_java;

public class New_011_Variables9 {

	public static void main(String[] args) {
		
		int a;                          // Declare a local variable
		a=10;                           // assigning the value to a variable
		System.out.println(a);          //10
		
		int b='d';                     // Defining the variable
		System.out.println(b);          // calling variable b
		
		String c = "a";
		System.out.println(c);         //a
		
		String Str ="akash";
		System.out.println(Str);      // akash

	}

}
