package learning_java;

public class New_007_forloopexamplessum {

	public static void main(String[] args) {
		 
		// Sum of natural number 1 to 10
		
		int sum=0;
		
		for (int i=0; i<=10; i++) {
			
			sum = sum +i;
			
			System.out.println("Number is" +sum );
		}

	}

}

// Output:- 
//Number is1
//Number is3
//Number is6
//Number is10
//Number is15
//Number is21
//Number is28
